public class Metodo
{
	public void circulo(double radio)
	{
		double areaCirculo;
		areaCirculo=3.1416*radio*radio;
		System.out.println("Valor de radio"+radio);
		System.out.println("Area es..."+areaCirculo);
	}
	public void cuadrado(double lado)
	{
		double areaCuadrado;
		areaCuadrado=lado*lado;
		System.out.println("El valor de lado es..."+lado);
		System.out.println("Area de cuadrado es..."+areaCuadrado);
	}
}
